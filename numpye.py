import numpy as np

# Create a NumPy array
arr = np.array([[10, 20, 30], [40, 50, 60]])

# a. Type of array
print("a. Type of array:", type(arr))

# b. Axes (number of dimensions)
print("b. Axes (number of dimensions):", arr.ndim)

# c. Shape of array
print("c. Shape of array:", arr.shape)

# d. Type of elements in array
print("d. Type of elements in array:", arr.dtype)
